"""
Escreva um programa que solicite ao usuário dois números e verifique se o
primeiro número é maior que o segundo. Imprima uma mensagem informando o resultado da comparação.   
"""
n1 = int(input("Numero: "))
n2 = int(input("Numero: "))

if n1 > n2:
    print("O primeiro numero é maior que o segundo.")
else:
    print("O Segundo é maior que o primeiro.")